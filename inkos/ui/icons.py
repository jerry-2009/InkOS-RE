def load_icon(path):
    if not path:
        return None
    try:
        with open(path, "rb") as fp:
            data = fp.read()
        if len(data) == 32:
            return data
        text = data.decode()
        rows = []
        for line in text.splitlines():
            line = line.strip()
            if not line:
                continue
            bits = 0
            for char in line[:16]:
                bits = (bits << 1) | (0 if char in ("0", ".", " ") else 1)
            rows.append((bits >> 8) & 0xFF)
            rows.append(bits & 0xFF)
            if len(rows) >= 32:
                return bytes(rows[:32])
    except Exception:
        pass
    return None
